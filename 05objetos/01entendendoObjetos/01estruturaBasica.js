
/* *** Um objeto é uma coleção de dinâmica de chave e valor ***
1º eu declarei a "variavel objeto": pessoa;
2º atribui uma coleção de chaves(nome, idade e peso ) e valores(nome : 'Leonan Rafael Malta', 27 e 73);
3º Manipulei as informações do objeto no consele 
*/

const pessoa = {
    nome : 'Leonan Rafael Malta',
    idade : 19,
    peso : 65,
};
console.log(pessoa.nome); // saída: Leonan Rafael Malta
console.log(pessoa.idade); // saída: 19
console.log(pessoa.peso); // saída: 65
console.log(pessoa); // saída: { nome: 'Leonan Rafael Malta', idade: 27, peso: 65 }